document.addEventListener("click", (event) => {
  if (!editModeEnabled) {
    return;
  }

  const anchor = event.target.closest("a");
  if (anchor && !anchor.classList.contains("editor-toggle")) {
    event.preventDefault();
  }
});

elements.editorToggle?.addEventListener("click", () => {
  if (!editModeEnabled) {
    const enteredPassword = window.prompt("Enter edit password");
    if (enteredPassword !== EDIT_MODE_PASSWORD) {
      window.alert("Incorrect password");
      return;
    }
    setEditMode(true);
    return;
  }

  setEditMode(false);
});


function clearNode(node) {
  if (!node) {
    return;
  }
  node.innerHTML = "";
}

function getStoredEdits() {
  try {
    return JSON.parse(localStorage.getItem(EDIT_STORAGE_KEY) || "{}");
  } catch (error) {
    return {};
  }
}

function writeStoredEdits(edits) {
  localStorage.setItem(EDIT_STORAGE_KEY, JSON.stringify(edits));
}

function getEditStorageKey(node) {
  return `${currentLang}:${node.dataset.editKey}`;
}

function normalizeEditableText(node) {
  const rawText = node.innerText.replace(/\u00a0/g, " ");
  const normalized = rawText
    .split("\n")
    .map((line) => line.trim())
    .filter(Boolean)
    .join(" ");
  return normalized;
}

function saveEditableNode(node) {
  const edits = getStoredEdits();
  const key = getEditStorageKey(node);
  const value = normalizeEditableText(node);

  node.textContent = value;
  edits[key] = value;
  writeStoredEdits(edits);
}

function applyStoredEdits() {
  const edits = getStoredEdits();
  document.querySelectorAll("[data-edit-key]").forEach((node) => {
    const value = edits[getEditStorageKey(node)];
    if (typeof value === "string") {
      node.textContent = value;
    }
  });
}

function setupEditableContent() {
  const editableNodes = [...document.querySelectorAll(EDITABLE_SELECTOR)];

  editableNodes.forEach((node, index) => {
    const stableKey = node.id || `${node.tagName.toLowerCase()}-${index}`;
    node.dataset.editKey = stableKey;
    node.classList.add("editable-node");
    node.setAttribute("spellcheck", "false");
    node.contentEditable = editModeEnabled ? "true" : "false";

    node.onfocus = null;
    node.onblur = null;
    node.onkeydown = null;
    node.oninput = null;

    if (editModeEnabled) {
      node.onfocus = () => node.classList.add("is-editing");
      node.onblur = () => {
        node.classList.remove("is-editing");
        saveEditableNode(node);
      };
      node.onkeydown = (event) => {
        if (event.key === "Escape") {
          node.blur();
        }
      };
      node.oninput = () => {
        clearTimeout(node._editTimer);
        node._editTimer = setTimeout(() => saveEditableNode(node), 250);
      };
    }
  });

  applyStoredEdits();
}

function setEditMode(nextState) {
  editModeEnabled = nextState;
  document.body.classList.toggle("edit-mode", editModeEnabled);

  if (elements.editorToggle) {
    elements.editorToggle.textContent = editModeEnabled ? "إنهاء" : "خليل";
    elements.editorToggle.setAttribute("aria-label", editModeEnabled ? "إنهاء وضع التعديل" : "فتح وضع التعديل");
  }

  setupEditableContent();
}

function getStoredPortrait() {
  try {
    return JSON.parse(localStorage.getItem(PORTRAIT_STORAGE_KEY) || "{}");
  } catch (error) {
    return {};
  }
}

function getActivePortrait() {
  return { ...DEFAULT_PORTRAIT, ...getStoredPortrait() };
}

function writeStoredPortrait(portrait) {
  localStorage.setItem(PORTRAIT_STORAGE_KEY, JSON.stringify(portrait));
}

function applyPortrait(portrait = getActivePortrait()) {
  if (!elements.portraitImage) {
    return;
  }

  const portraitCore = elements.portraitImage.closest(".portrait-core");
  const hasImage = Boolean(portrait.image);

  elements.portraitImage.src = portrait.image || "";
  elements.portraitImage.style.setProperty("--portrait-scale", portrait.scale || DEFAULT_PORTRAIT.scale);
  elements.portraitImage.style.setProperty("--portrait-x", `${portrait.x || 0}%`);
  elements.portraitImage.style.setProperty("--portrait-y", `${portrait.y || 0}%`);
  portraitCore?.classList.toggle("has-image", hasImage);

  if (elements.portraitZoom) elements.portraitZoom.value = portrait.scale || DEFAULT_PORTRAIT.scale;
  if (elements.portraitX) elements.portraitX.value = portrait.x || "0";
  if (elements.portraitY) elements.portraitY.value = portrait.y || "0";
}

function updatePortraitSettings(partial) {
  const portrait = { ...DEFAULT_PORTRAIT, ...getStoredPortrait(), ...partial };
  writeStoredPortrait(portrait);
  applyPortrait(portrait);
}

function setupPortraitEditor() {
  applyPortrait();

  elements.portraitUploadInput?.addEventListener("change", (event) => {
    const file = event.target.files?.[0];
    if (!file) {
      return;
    }

    const reader = new FileReader();
    reader.onload = () => {
      updatePortraitSettings({
        image: String(reader.result),
        scale: elements.portraitZoom?.value || DEFAULT_PORTRAIT.scale,
        x: elements.portraitX?.value || "0",
        y: elements.portraitY?.value || "0"
      });
    };
    reader.readAsDataURL(file);
  });

  elements.portraitZoom?.addEventListener("input", (event) => {
    updatePortraitSettings({ scale: event.target.value });
  });

  elements.portraitX?.addEventListener("input", (event) => {
    updatePortraitSettings({ x: event.target.value });
  });

  elements.portraitY?.addEventListener("input", (event) => {
    updatePortraitSettings({ y: event.target.value });
  });

  elements.portraitReset?.addEventListener("click", () => {
    localStorage.removeItem(PORTRAIT_STORAGE_KEY);
    applyPortrait(DEFAULT_PORTRAIT);
    if (elements.portraitUploadInput) {
      elements.portraitUploadInput.value = "";
    }
  });
}

function getContactKind(item) {
  const href = String(item?.href || "").toLowerCase();
  if (!href) return null;
  if (href.startsWith("mailto:")) return "email";
  if (href.includes("wa.me") || href.includes("whatsapp")) return "whatsapp";
  if (href.includes("linkedin.com")) return "linkedin";
  return null;
}


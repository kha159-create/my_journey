function createLinkOrSpan(item, className) {
  const node = item.href ? document.createElement("a") : document.createElement("span");
  node.className = className;
  node.textContent = item.label;

  if (item.href) {
    node.href = item.href;
    if (item.href.startsWith("http")) {
      node.target = "_blank";
      node.rel = "noreferrer";
    }
  }

  return node;
}


function setupTimelineObserver() {
  const entries = [...document.querySelectorAll(".timeline-entry")];
  const navItems = [...document.querySelectorAll(".timeline-nav li")];

  const activateIndex = (index) => {
    entries.forEach((entry, entryIndex) => {
      entry.classList.toggle("is-active", entryIndex === index);
    });

    navItems.forEach((item, itemIndex) => {
      item.classList.toggle("is-active", itemIndex === index);
    });

    const progressRatio = (index + 1) / entries.length;
    if (window.innerWidth <= 1220) {
      elements.railProgress.style.width = `${progressRatio * 100}%`;
      elements.railProgress.style.height = "1px";
    } else {
      elements.railProgress.style.height = `${progressRatio * 100}%`;
      elements.railProgress.style.width = "1px";
    }
  };

  if (entries.length > 0) {
    activateIndex(0);
  }

  if (window.timelineObserver) {
    window.timelineObserver.disconnect();
  }

  window.timelineObserver = new IntersectionObserver(
    (observedEntries) => {
      const visibleEntry = observedEntries
        .filter((entry) => entry.isIntersecting)
        .sort((a, b) => b.intersectionRatio - a.intersectionRatio)[0];

      if (visibleEntry) {
        activateIndex(Number(visibleEntry.target.dataset.index));
      }
    },
    { threshold: [0.25, 0.5, 0.75], rootMargin: "-8% 0px -20% 0px" }
  );

  entries.forEach((entry) => window.timelineObserver.observe(entry));
}

function setupReveal() {
  const revealElements = document.querySelectorAll(".reveal");

  if (window.revealObserver) {
    window.revealObserver.disconnect();
  }

  window.revealObserver = new IntersectionObserver(
    (entries) => {
      entries.forEach((entry) => {
        if (entry.isIntersecting) {
          entry.target.classList.add("is-visible");
          window.revealObserver.unobserve(entry.target);
        }
      });
    },
    { threshold: 0.15, rootMargin: "0px 0px -8% 0px" }
  );

  revealElements.forEach((element) => {
    element.classList.remove("is-visible");
    window.revealObserver.observe(element);
  });
}

